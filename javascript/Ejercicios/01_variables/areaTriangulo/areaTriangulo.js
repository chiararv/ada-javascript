// Crear un programa que pida al usuario ingresar el valor de la base y el valor de la altura de un triangulo, calcular su Ã¡rea y mostrarlo en un mensaje.

let base = prompt("Ingresa la base del triangulo");
let altura = prompt("Ingresa la altura del triangulo");
let area = (base * altura) / 2;

alert(`El area del triangulo es ${area}`);
